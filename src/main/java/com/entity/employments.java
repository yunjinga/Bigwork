package com.entity;

public class employments {
    private int EmployNo ;
    private String EmplopyName ;
    private String EmploySex ;
    private int EmployAge ;
    private String EmployDept ;
    public int getEmployNo() {
        return EmployNo;
    }

    public void setEmployNo(int employNo) {
        EmployNo = employNo;
    }

    public String getEmplopyName() {
        return EmplopyName;
    }

    public void setEmplopyName(String emplopyName) {
        EmplopyName = emplopyName;
    }

    public String getEmploySex() {
        return EmploySex;
    }

    public void setEmploySex(String employSex) {
        EmploySex = employSex;
    }

    public int getEmployAge() {
        return EmployAge;
    }

    public void setEmployAge(int employAge) {
        EmployAge = employAge;
    }

    public String getEmployDept() {
        return EmployDept;
    }

    public void setEmployDept(String employDept) {
        EmployDept = employDept;
    }
}
